package ec.renafipse.mks.clase;

/**
 *
 * @author andres
 */
public abstract class ErrorEjecucionPaquete {
    
    /**
     *
     */
    protected String mensajeFormulario;

    /**
     *
     */
    protected Long codigoFormulario;

    /**
     *
     */
    protected Long generaFormulario;

    /**
     *
     */
    protected Long codigoSalida;

    /**
     *
     */
    protected String errorSql;

    /**
     *
     */
    protected String errorCode;

    /**
     *
     */
    protected String error;

    /**
     *
     */
    public ErrorEjecucionPaquete() {
    }

    /**
     *
     * @param mensajeFormulario
     * @param codigoFormulario
     * @param generaFormulario
     * @param codigoSalida
     * @param errorSql
     * @param errorCode
     * @param error
     */
    public ErrorEjecucionPaquete(String mensajeFormulario, Long codigoFormulario, Long generaFormulario, Long codigoSalida, String errorSql, String errorCode, String error) {
        this.mensajeFormulario = mensajeFormulario;
        this.codigoFormulario = codigoFormulario;
        this.generaFormulario = generaFormulario;
        this.codigoSalida = codigoSalida;
        this.errorSql = errorSql;
        this.errorCode = errorCode;
        this.error = error;
    }
    
    /**
     *
     * @return
     */
    public String getMensajeFormulario() {
        return mensajeFormulario;
    }

    /**
     *
     * @param mensajeFormulario
     */
    public void setMensajeFormulario(String mensajeFormulario) {
        this.mensajeFormulario = mensajeFormulario;
    }

    /**
     *
     * @return
     */
    public Long getCodigoFormulario() {
        return codigoFormulario;
    }

    /**
     *
     * @param codigoFormulario
     */
    public void setCodigoFormulario(Long codigoFormulario) {
        this.codigoFormulario = codigoFormulario;
    }

    /**
     *
     * @return
     */
    public Long getGeneraFormulario() {
        return generaFormulario;
    }

    /**
     *
     * @param generaFormulario
     */
    public void setGeneraFormulario(Long generaFormulario) {
        this.generaFormulario = generaFormulario;
    }

    /**
     *
     * @return
     */
    public Long getCodigoSalida() {
        return codigoSalida;
    }

    /**
     *
     * @param codigoSalida
     */
    public void setCodigoSalida(Long codigoSalida) {
        this.codigoSalida = codigoSalida;
    }

    /**
     *
     * @return
     */
    public String getErrorSql() {
        return errorSql;
    }

    /**
     *
     * @param errorSql
     */
    public void setErrorSql(String errorSql) {
        this.errorSql = errorSql;
    }

    /**
     *
     * @return
     */
    public String getErrorCode() {
        return errorCode;
    }

    /**
     *
     * @param errorCode
     */
    public void setErrorCode(String errorCode) {
        this.errorCode = errorCode;
    }

    /**
     *
     * @return
     */
    public String getError() {
        return error;
    }

    /**
     *
     * @param error
     */
    public void setError(String error) {
        this.error = error;
    }
    
    @Override
    public String toString() {
        return "ErrorEjecucionPaquete{" + "mensajeFormulario=" + mensajeFormulario + ", codigoFormulario=" + codigoFormulario + ", generaFormulario=" + generaFormulario + ", codigoSalida=" + codigoSalida + ", errorSql=" + errorSql + ", errorCode=" + errorCode + ", error=" + error + '}';
    }
}
